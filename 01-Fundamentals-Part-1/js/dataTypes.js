// number = floating point number, ex. 23 or 12.9;
// string = sequence of characters, ex 'words';
// boolean = true or false;
// undefined = empty value;
// null = empty value;
// symbol = unique value, can't be changed;
// bigInt = large integers that number can't hold;

console.log(true);

